num1 = 10
num2 = 2
# Arithmetic Operators
print("num1 + num2 is ", num1 + num2)
print("num1 - num2 is ", num1 - num2)
print("num1 * num2 is ", num1 * num2)
print("num1 / num2 is ", num1 / num2)
# float division
print("num1 // num2 is ", num1 // num2)
# exponentiation operator
print("num1 ** num2 is ", num1 ** num2)
print("num1 % num2 is ", num1 % num2)

